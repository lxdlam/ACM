// Constants here
unordered_map<ll, ll> memo;

const int SIZE = 1e6 + 10;

int prime[SIZE + 1] = {0};
void getPrime() {
  for (int i = 2; i <= SIZE; i++) {
    if (!prime[i]) prime[++prime[0]] = i;
    for (int j = 1; j <= prime[0] && prime[j] <= SIZE / i; j++) {
      prime[prime[j] * i] = 1;
      if (i % prime[j] == 0) break;
    }
  }
}

ll count(ll n) {
  ll k = n;
  if (!memo.count(n)) {
    ll ans = 1;

    for (ll i = 1; i <= prime[0] && prime[i] * prime[i] <= n; i++) {
      if (n % prime[i] == 0) {
        ll cnt = 0;
        ll p = prime[i];
        while (n % p == 0) {
          n /= p;
          cnt++;
        }

        ans = ans * (cnt + 1);
      }
    }

    if (n != 1) ans *= 2;
    memo[k] = ans;
  }

  return memo[k];
}

bool check(ll n) {
  if (n % 4 == 2) return false;
  if (n % 4 == 0) return (count(n / 4) + 1) / 2 >= 2;
  return (count(n) + 1) / 2 >= 2;
}

// Pre-Build Function
inline void build() { getPrime(); }

// Actual Solver
inline void solve() {
  tcase() {
    cout << "Case #" << kase << ": ";
    int l, r;
    int ans = 0;
    readln(l, r);
    for (; l <= r; l++) {
      if (!check(l)) {
        ans++;
      }
    }
    cout << ans << '\n';
  }
}

