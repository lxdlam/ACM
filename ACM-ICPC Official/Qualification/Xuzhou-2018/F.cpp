// Constants here
struct my_hash
{
    template <class T, class U>
    size_t operator()(const pair<T, U> &p) const
    {
        auto lhs = hash<T>{}(p.first);
        auto rhs = hash<U>{}(p.second);
        return lhs ^ rhs;
    }
};

using ht = gp_hash_table<pair<ll, ll>, ll, my_hash>;

// Pre-Build Function
inline void build()
{
}

// Actual Solver
inline void solve()
{
    tcase()
    {
        int n;
        cin >> n;
        ht m;
        ll ans = 0;
        while (n--)
        {
            int k;
            cin >> k;
            ht t;
            ll x, y;
            while (k--)
            {
                cin >> x >> y;
                auto p = mp(x, y);
                if (m.find(p) != m.end())
                    t[p] = m[p] + 1;
                else
                    t[p] = 1;
                smax(ans, t[p]);
            }
            m = t;
        }
        cout << ans << '\n';
    }
}

