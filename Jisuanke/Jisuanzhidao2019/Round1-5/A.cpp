// Actual Solver
inline void solve() {
  ll m, k, q;
  readln(m, k, q);

  set<ll> s;

  if (m == 1) {
    while (q--) {
      ll op, num;
      cin >> op >> num;

      if (op == 1) {
        s.insert(num);
      } else if (op == 2) {
        s.erase(num);
      }

      if (s.empty())
        cout << k << '\n';
      else
        cout << *s.begin() << '\n';
    }
  } else {
    ll ans;

    while (q--) {
      ll op, num;
      cin >> op >> num;
      num++;

      if (op == 1) {
        s.insert(num);
      } else if (op == 2) {
        s.erase(num);
      }

      ans = (1LL << k) - 1;

      for (auto it : s) {
        bool f = true;
        ll cur = it;

        while (cur) {
          if (s.count(cur / 2)) {
            f = false;
            break;
          }
          cur /= 2;
        }

        if (f) {
          ll p = 0;
          while ((1LL << p) <= it) p++;

          ans -= (1LL << (k - p + 1)) - 1;
        }
      }

      cout << ans << '\n';
    }
  }
}

